@st.composite
def st_log_entry(draw) -> dict:
    """Generate a valid session log dict.

    Fields: timestamp (ISO 8601), session_id (text), module (1-11),
    step (1-10), event ("turn" or "correction"), duration_seconds (0-600),
    message (text).
    """
    year = draw(st.integers(min_value=2020, max_value=2039))
    month = draw(st.integers(min_value=1, max_value=12))
    day = draw(st.integers(min_value=1, max_value=28))
    hour = draw(st.integers(min_value=0, max_value=23))
    minute = draw(st.integers(min_value=0, max_value=59))
    second = draw(st.integers(min_value=0, max_value=59))
    timestamp = f"{year:04d}-{month:02d}-{day:02d}T{hour:02d}:{minute:02d}:{second:02d}Z"

    session_id = draw(st.text(min_size=1, max_size=20, alphabet=st.characters(
        whitelist_categories=("L", "N"),
    )))
    module = draw(st.integers(min_value=1, max_value=11))
    step = draw(st.integers(min_value=1, max_value=10))
    event = draw(st.sampled_from(("turn", "correction")))
    duration_seconds = draw(st.floats(min_value=0.0, max_value=600.0))
    message = draw(st.text(min_size=0, max_size=100))

    return {
        "timestamp": timestamp,
        "session_id": session_id,
        "module": module,
        "step": step,
        "event": event,
        "duration_seconds": duration_seconds,
        "message": message,
    }


@st.composite
def st_log_entries(draw) -> list[dict]:
    """Generate a list of valid session log entry dicts.

    Produces between 0 and 50 entries using the st_log_entry strategy.
    """
    return draw(st.lists(st_log_entry(), min_size=0, max_size=50))


@st.composite
def st_skip_record(draw) -> SkipRecord:
    """Generate a valid SkipRecord instance.

    Fields: module_step like "N.M" (module 1-11, step 1-10),
    reason in ("a", "b", "c"), note (text), skipped_at (ISO 8601).
    """
    module = draw(st.integers(min_value=1, max_value=11))
    step = draw(st.integers(min_value=1, max_value=10))
    module_step = f"{module}.{step}"

    reason = draw(st.sampled_from(("a", "b", "c")))
    note = draw(st.text(min_size=0, max_size=100))

    year = draw(st.integers(min_value=2020, max_value=2039))
    month = draw(st.integers(min_value=1, max_value=12))
    day = draw(st.integers(min_value=1, max_value=28))
    hour = draw(st.integers(min_value=0, max_value=23))
    minute = draw(st.integers(min_value=0, max_value=59))
    second = draw(st.integers(min_value=0, max_value=59))
    skipped_at = f"{year:04d}-{month:02d}-{day:02d}T{hour:02d}:{minute:02d}:{second:02d}Z"

    return SkipRecord(
        module_step=module_step,
        reason=reason,
        note=note,
        skipped_at=skipped_at,
    )


