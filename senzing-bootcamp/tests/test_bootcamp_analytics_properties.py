class TestComputeModuleMetricsProperties:
    """Property-based tests for compute_module_metrics.

    **Validates: Requirements 2, 10**
    """

    @given(st_log_entries())
    @settings(max_examples=10)
    def test_total_turns_equals_input_entry_count(
        self, entries: list[dict]
    ) -> None:
        """Sum of turn_count across all ModuleMetrics equals input entry count.

        Since all entries generated by st_log_entries have a valid integer
        module field, every entry is counted as a turn in exactly one module.
        """
        metrics = compute_module_metrics(entries)
        total_turns = sum(m.turn_count for m in metrics)
        assert total_turns == len(entries)


class TestDetectFrictionPointsProperties:
    """Property-based tests for detect_friction_points.

    **Validates: Requirements 3, 10**
    """

    VALID_CATEGORIES = {"slow", "high_corrections", "skipped", "mcp_failures"}
    VALID_SEVERITIES = {"high", "medium", "low"}

    @given(st_log_entries(), st.lists(st_skip_record(), min_size=0, max_size=10))
    @settings(max_examples=10)
    def test_returns_only_valid_categories_and_severities(
        self, entries: list[dict], skips: list[SkipRecord]
    ) -> None:
        """All FrictionPoints have valid category and severity values.

        For any generated log entries and skip records, every FrictionPoint
        returned by detect_friction_points must have:
        - category in {"slow", "high_corrections", "skipped", "mcp_failures"}
        - severity in {"high", "medium", "low"}
        """
        metrics = compute_module_metrics(entries)
        friction_points = detect_friction_points(metrics, skips)

        for fp in friction_points:
            assert fp.category in self.VALID_CATEGORIES, (
                f"Invalid category: {fp.category!r}"
            )
            assert fp.severity in self.VALID_SEVERITIES, (
                f"Invalid severity: {fp.severity!r}"
            )


class TestFormatJsonReportProperties:
    """Property-based tests for format_json_report.

    **Validates: Requirements 4, 10**
    """

    @given(st_log_entries(), st.lists(st_skip_record(), min_size=0, max_size=10))
    @settings(max_examples=10)
    def test_format_json_report_always_produces_valid_json(
        self, entries: list[dict], skips: list[SkipRecord]
    ) -> None:
        """format_json_report always produces valid JSON for any AnalyticsReport.

        For any generated log entries and skip records, building an
        AnalyticsReport and calling format_json_report always produces output
        that json.loads can parse without raising.
        """
        metrics = compute_module_metrics(entries)
        friction_points = detect_friction_points(metrics, skips)

        report = AnalyticsReport(
            module_metrics=metrics,
            friction_points=friction_points,
            skipped_steps=skips,
            total_time_seconds=sum(m.total_seconds for m in metrics),
            total_turns=sum(m.turn_count for m in metrics),
            total_corrections=sum(m.correction_count for m in metrics),
            baseline_comparison=None,
        )

        result = format_json_report(report)
        parsed = json.loads(result)
        assert isinstance(parsed, dict)


class TestParseSessionLogProperties:
    """Property-based tests for parse_session_log.

    **Validates: Requirements 1, 5, 10**
    """

    @given(st.text(max_size=1000))
    @settings(max_examples=10)
    def test_parse_session_log_never_raises_on_arbitrary_content(
        self, content: str
    ) -> None:
        """parse_session_log never raises on arbitrary file content.

        For any arbitrary text content written to a file, parse_session_log
        always returns a list gracefully without raising an exception.
        """
        import tempfile
        import os

        fd, tmp_path = tempfile.mkstemp(suffix=".jsonl")
        try:
            with os.fdopen(fd, "w", encoding="utf-8") as fh:
                fh.write(content)
            result = parse_session_log(tmp_path)
            assert isinstance(result, list)
        finally:
            os.unlink(tmp_path)
